#pragma once

#include <iostream>

template<typename T>
class Node {
    public:
        // *************************************************
        //
        // constructors default/copy/paramatized/destruct
        //
        // *************************************************
        Node();
        Node(const Node& copy);
        Node(const T item);
        ~Node();

        // *************************************************
        //
        // assignment operator
        //
        // *************************************************
        Node& operator=(const Node& rhs);

        // *************************************************
        //
        // print node
        //
        // *************************************************
        void print();
   
        // *************************************************
        //
        // default pointers assigned to each node at start
        //
        // *************************************************
        Node<T>* pNext;
        Node<T>* pPrev;

        // *************************************************
        //
        // Data members
        // 
        // *************************************************
        T data;
};

