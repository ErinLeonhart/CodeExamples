#pragma once

#include "stack.h"

// *************************************************
//
//  add new items to a stack 
//
// *************************************************
template<typename T>
void Stack<T>::push(T item)
{
    // ********************************************
    // use prebuilt function in LList to add
    // ********************************************
    LList<T>::addToHead(item);
}

// *************************************************
//
//  add new items to a stack 
//
// *************************************************
template <typename T>
T Stack<T>::pop()
{
    // *********************************************
    // use prebuilt function to get and store item
    // *********************************************
    T item = LList<T>::peekAtHead();

    // *********************************************
    // use prebuilt function in LList to remove
    // *********************************************
    LList<T>::removeAtHead();

    // *********************************************
    // return the item removed
    // *********************************************
    return item;
}
