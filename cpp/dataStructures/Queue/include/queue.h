#pragma once

#include <iostream>
#include "llist.cpp"

template<typename T>
class Queue : public LList<T>
{
public:
    // *************************************************
    //
    //  add new items to a Queue
    //
    // *************************************************
    void enqueue(T item);

    // *************************************************
    //
    //  remove an item from the Queue
    //
    // *************************************************
    T dequeue();
};

